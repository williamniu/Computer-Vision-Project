def retrieve_similar_images(
    query_index,
    normalized_embeddings,
    metadata_df,
    top_k=5,
    retrieval_filter="No filter",
    auto_species_label=None
):
    """
    Retrieve top-k similar dataset images for a selected dataset query index.
    """

    query_embedding = normalized_embeddings[int(query_index)]

    similarity_scores = normalized_embeddings @ query_embedding

    allowed_indexes = get_allowed_retrieval_indexes(
        metadata_df=metadata_df,
        retrieval_filter=retrieval_filter,
        auto_species_label=auto_species_label
    )

    # Mask out disallowed examples.
    masked_scores = np.full_like(similarity_scores, fill_value=-np.inf, dtype=np.float32)
    masked_scores[allowed_indexes] = similarity_scores[allowed_indexes]

    # Exclude the query image itself.
    masked_scores[int(query_index)] = -np.inf

    top_indexes = np.argsort(masked_scores)[::-1][:top_k]

    results_df = metadata_df.iloc[top_indexes].copy()
    results_df.insert(0, "retrieval_rank", range(1, len(top_indexes) + 1))
    results_df.insert(1, "retrieved_index", top_indexes)
    results_df.insert(2, "similarity_score", masked_scores[top_indexes])

    query_label = metadata_df.loc[int(query_index), "breed_label"]
    query_breed = metadata_df.loc[int(query_index), "breed_name"]

    results_df["query_index"] = int(query_index)
    results_df["query_breed_label"] = query_label
    results_df["query_breed_name"] = query_breed
    results_df["same_breed_as_query"] = results_df["breed_label"] == query_label

    return results_df


def retrieve_similar_images_from_embedding(
    query_embedding,
    normalized_embeddings,
    metadata_df,
    top_k=5,
    retrieval_filter="No filter",
    auto_species_label=None
):
    """
    Retrieve top-k similar dataset images for an uploaded image embedding.
    """

    query_embedding = query_embedding.reshape(1, -1)

    query_norm = np.linalg.norm(query_embedding, axis=1, keepdims=True)
    query_norm = np.maximum(query_norm, 1e-12)
    query_embedding = query_embedding / query_norm

    similarity_scores = normalized_embeddings @ query_embedding[0]

    allowed_indexes = get_allowed_retrieval_indexes(
        metadata_df=metadata_df,
        retrieval_filter=retrieval_filter,
        auto_species_label=auto_species_label
    )

    masked_scores = np.full_like(similarity_scores, fill_value=-np.inf, dtype=np.float32)
    masked_scores[allowed_indexes] = similarity_scores[allowed_indexes]

    top_indexes = np.argsort(masked_scores)[::-1][:top_k]

    results_df = metadata_df.iloc[top_indexes].copy()
    results_df.insert(0, "retrieval_rank", range(1, len(top_indexes) + 1))
    results_df.insert(1, "retrieved_index", top_indexes)
    results_df.insert(2, "similarity_score", masked_scores[top_indexes])

    return results_df


