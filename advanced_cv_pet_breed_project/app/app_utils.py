@st.cache_data
def load_retrieval_metadata():
    """
    Load retrieval metadata generated by Notebook 04.
    """

    metadata_path = EMBEDDING_DIR / "04_pet_image_retrieval_metadata.csv"

    if not metadata_path.exists():
        raise FileNotFoundError(f"Missing retrieval metadata: {metadata_path}")

    return pd.read_csv(metadata_path)


@st.cache_data
def load_embeddings():
    """
    Load MobileNetV2 image embeddings generated by Notebook 04.
    """

    embeddings_path = EMBEDDING_DIR / "04_mobilenetv2_pet_image_embeddings.npy"

    if not embeddings_path.exists():
        raise FileNotFoundError(f"Missing embeddings file: {embeddings_path}")

    embeddings = np.load(embeddings_path)

    # Normalize embeddings so dot product is cosine similarity.
    norms = np.linalg.norm(embeddings, axis=1, keepdims=True)
    norms = np.maximum(norms, 1e-12)

    normalized_embeddings = embeddings / norms

    return normalized_embeddings


@st.cache_data(ttl=60)
def load_style_image_paths():
    """
    Load available style image paths from assets/styles.

    The ttl setting lets the app refresh the style list periodically.
    """

    if not STYLE_DIR.exists():
        STYLE_DIR.mkdir(parents=True, exist_ok=True)

    style_paths = sorted(
        [
            path for path in STYLE_DIR.iterdir()
            if path.is_file() and path.suffix.lower() in [".jpg", ".jpeg", ".png"]
        ]
    )

    return style_paths


@st.cache_resource
def load_tfds_splits():
    """
    Load Oxford-IIIT Pet train and test splits.

    The retrieval corpus order is:
    1. train split,
    2. test split.
    """

    (ds_train_raw, ds_test_raw), ds_info = tfds.load(
        "oxford_iiit_pet",
        split=["train", "test"],
        as_supervised=False,
        with_info=True,
        shuffle_files=False
    )

    num_train = ds_info.splits["train"].num_examples
    num_test = ds_info.splits["test"].num_examples

    return ds_train_raw, ds_test_raw, ds_info, num_train, num_test


