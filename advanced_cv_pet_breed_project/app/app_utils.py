def infer_species_label_for_breed(metadata_df, breed_label):
    """
    Infer the species label associated with a predicted breed label.

    This is useful for uploaded images because the classifier predicts breed,
    while the retrieval filter can use species to avoid returning cats for dogs
    or dogs for cats.
    """

    subset = metadata_df.loc[metadata_df["breed_label"] == int(breed_label)]

    if subset.empty:
        return None

    return int(subset["species_label"].mode().iloc[0])


def get_allowed_retrieval_indexes(metadata_df, retrieval_filter="No filter", auto_species_label=None):
    """
    Return retrieval corpus indexes allowed by the selected species filter.

    Parameters
    ----------
    metadata_df : pd.DataFrame
        Retrieval metadata table.

    retrieval_filter : str
        One of "Auto", "Dog only", "Cat only", or "No filter".

    auto_species_label : int or None
        Species label to use when retrieval_filter is "Auto".

    Returns
    -------
    np.ndarray
        Array of allowed retrieval indexes.
    """

    if retrieval_filter == "No filter":
        return np.arange(len(metadata_df))

    if retrieval_filter == "Dog only":
        return metadata_df.index[metadata_df["species_name"].str.lower() == "dog"].to_numpy()

    if retrieval_filter == "Cat only":
        return metadata_df.index[metadata_df["species_name"].str.lower() == "cat"].to_numpy()

    if retrieval_filter == "Auto" and auto_species_label is not None:
        return metadata_df.index[metadata_df["species_label"] == int(auto_species_label)].to_numpy()

    # If auto filtering cannot be resolved, fall back to no filter.
    return np.arange(len(metadata_df))


