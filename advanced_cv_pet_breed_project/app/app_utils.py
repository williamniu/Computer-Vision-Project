def pil_image_to_display_array(pil_image):
    """
    Convert a PIL image to a display-ready NumPy array.
    """

    image = pil_image.convert("RGB")
    image = image.resize(IMAGE_SIZE)

    image_array = np.array(image).astype("float32") / 255.0

    return image_array


def pil_image_to_mobilenet_batch(pil_image):
    """
    Convert a PIL image to a MobileNetV2-preprocessed batch.
    """

    image = pil_image.convert("RGB")
    image = image.resize(IMAGE_SIZE)

    image_array = np.array(image).astype("float32")
    image_batch = np.expand_dims(image_array, axis=0)

    image_batch = mobilenet_preprocess_input(image_batch)

    return image_batch


def pil_image_to_content_tensor(pil_image, max_dim=256):
    """
    Convert a PIL image to a batched content tensor for style transfer.
    """

    image = pil_image.convert("RGB")
    image_array = np.array(image).astype("float32") / 255.0
    image_tensor = tf.convert_to_tensor(image_array)

    return resize_image_preserve_aspect(image_tensor, max_dim=max_dim)


def classify_image(pil_image, classifier_model, breed_names):
    """
    Predict the pet breed for an uploaded PIL image.
    """

    image_batch = pil_image_to_mobilenet_batch(pil_image)

    probabilities = classifier_model.predict(image_batch, verbose=0)[0]

    predicted_index = int(np.argmax(probabilities))
    predicted_breed = breed_names[predicted_index]
    predicted_confidence = float(probabilities[predicted_index])

    top_indexes = np.argsort(probabilities)[::-1][:5]

    top_predictions_df = pd.DataFrame(
        [
            {
                "rank": rank + 1,
                "breed": breed_names[index],
                "confidence": float(probabilities[index])
            }
            for rank, index in enumerate(top_indexes)
        ]
    )

    return predicted_index, predicted_breed, predicted_confidence, top_predictions_df


def extract_uploaded_embedding(pil_image, feature_extractor):
    """
    Extract a MobileNetV2 embedding for an uploaded PIL image.
    """

    image_batch = pil_image_to_mobilenet_batch(pil_image)
    embedding = feature_extractor.predict(image_batch, verbose=0)[0]

    return embedding


