def load_style_image_from_path(image_path, max_dim=256):
    """
    Load a style image from disk as a batched tensor scaled to [0, 1].
    """

    image_bytes = tf.io.read_file(str(image_path))
    image = tf.image.decode_image(image_bytes, channels=3)
    image = tf.image.convert_image_dtype(image, tf.float32)

    return resize_image_preserve_aspect(image, max_dim=max_dim)


def tensor_to_image(image_tensor):
    """
    Convert a batched or unbatched float image tensor in [0, 1] to uint8 array.
    """

    if len(image_tensor.shape) == 4:
        image_tensor = image_tensor[0]

    image_tensor = tf.clip_by_value(image_tensor, 0.0, 1.0)
    image_array = tf.cast(image_tensor * 255.0, tf.uint8).numpy()

    return image_array


def gram_matrix(input_tensor):
    """
    Compute Gram matrix for style representation.
    """

    result = tf.linalg.einsum("bijc,bijd->bcd", input_tensor, input_tensor)

    input_shape = tf.shape(input_tensor)
    num_locations = tf.cast(input_shape[1] * input_shape[2], tf.float32)

    return result / num_locations


def vgg_layers(layer_names):
    """
    Create a VGG19 model that returns selected intermediate layer outputs.
    """

    vgg = VGG19(include_top=False, weights="imagenet")
    vgg.trainable = False

    outputs = [vgg.get_layer(name).output for name in layer_names]

    model = keras.Model([vgg.input], outputs)

    return model


class StyleContentModel(keras.models.Model):
    """
    Model that returns style and content dictionaries for an input image.
    """

    def __init__(self, style_layers, content_layers):
        super().__init__()

        self.vgg = vgg_layers(style_layers + content_layers)
        self.style_layers = style_layers
        self.content_layers = content_layers
        self.num_style_layers = len(style_layers)

        self.vgg.trainable = False

    def call(self, inputs):
        """
        Compute style and content features for the input image.
        """

        inputs = inputs * 255.0
        preprocessed_inputs = vgg_preprocess_input(inputs)

        outputs = self.vgg(preprocessed_inputs)

        style_outputs = outputs[:self.num_style_layers]
        content_outputs = outputs[self.num_style_layers:]

        style_outputs = [gram_matrix(style_output) for style_output in style_outputs]

        style_dict = {
            style_name: value
            for style_name, value in zip(self.style_layers, style_outputs)
        }

        content_dict = {
            content_name: value
            for content_name, value in zip(self.content_layers, content_outputs)
        }

        return {
            "style": style_dict,
            "content": content_dict
        }


def run_style_transfer(
    content_image,
    style_image,
    style_layers,
    content_layers,
    style_weight=1e-2,
    content_weight=1e4,
    total_variation_weight=30,
    epochs=3,
    steps_per_epoch=10,
    optimizer_lr=0.02
):
    """
    Run neural style transfer for one content/style pair.
    """

    extractor = StyleContentModel(style_layers, content_layers)

    style_targets = extractor(style_image)["style"]
    content_targets = extractor(content_image)["content"]

    num_style_layers = len(style_layers)
    num_content_layers = len(content_layers)

    def style_content_loss(outputs):
        """
        Compute combined style and content loss.
        """

        style_outputs = outputs["style"]
        style_loss = tf.add_n([
            tf.reduce_mean((style_outputs[name] - style_targets[name]) ** 2)
            for name in style_outputs.keys()
        ])
        style_loss *= style_weight / num_style_layers

        content_outputs = outputs["content"]
        content_loss = tf.add_n([
            tf.reduce_mean((content_outputs[name] - content_targets[name]) ** 2)
            for name in content_outputs.keys()
        ])
        content_loss *= content_weight / num_content_layers

        return style_loss + content_loss

    image = tf.Variable(content_image)

    optimizer = tf.keras.optimizers.Adam(
        learning_rate=optimizer_lr,
        beta_1=0.99,
        epsilon=1e-1
    )

    @tf.function()
    def train_step(image_variable):
        """
        Run one style-transfer optimization step.
        """

        with tf.GradientTape() as tape:
            outputs = extractor(image_variable)
            loss = style_content_loss(outputs)
            loss += total_variation_weight * tf.reduce_sum(
                tf.image.total_variation(image_variable)
            )

        gradients = tape.gradient(loss, image_variable)

        optimizer.apply_gradients([(gradients, image_variable)])

        image_variable.assign(tf.clip_by_value(image_variable, 0.0, 1.0))

        return loss

    progress = st.progress(0)
    status = st.empty()

    total_steps = epochs * steps_per_epoch
    step_counter = 0

    for epoch in range(epochs):
        for step in range(steps_per_epoch):
            loss_value = train_step(image)
            step_counter += 1

            progress.progress(step_counter / total_steps)
            status.write(
                f"Style transfer step {step_counter}/{total_steps} "
                f"- loss: {float(loss_value.numpy()):.4f}"
            )

    progress.empty()
    status.empty()

    return tensor_to_image(image)
