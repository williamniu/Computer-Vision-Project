def fetch_raw_example_by_index(index, ds_train_raw, ds_test_raw, num_train):
    """
    Fetch one raw Oxford-IIIT Pet example by retrieval-corpus index.
    """

    index = int(index)

    if index < num_train:
        ds_subset = ds_train_raw.skip(index).take(1)
    else:
        ds_subset = ds_test_raw.skip(index - num_train).take(1)

    for example in ds_subset:
        return example

    raise IndexError(f"Could not fetch dataset example for index: {index}")


@st.cache_data(show_spinner=False)
def get_display_image_by_index(index):
    """
    Load one dataset image for Streamlit display.
    """

    ds_train_raw, ds_test_raw, ds_info, num_train, num_test = load_tfds_splits()

    example = fetch_raw_example_by_index(
        index=int(index),
        ds_train_raw=ds_train_raw,
        ds_test_raw=ds_test_raw,
        num_train=num_train
    )

    image = example["image"]
    image = tf.image.resize(image, IMAGE_SIZE)
    image = tf.cast(image, tf.float32) / 255.0

    return image.numpy()


def resize_image_preserve_aspect(image, max_dim=256):
    """
    Resize an image tensor while preserving aspect ratio and add a batch dimension.
    """

    shape = tf.cast(tf.shape(image)[:-1], tf.float32)
    long_dim = tf.reduce_max(shape)
    scale = max_dim / long_dim
    new_shape = tf.cast(shape * scale, tf.int32)

    image = tf.image.resize(image, new_shape)
    image = image[tf.newaxis, :]

    return image


def get_content_tensor_from_dataset_index(index, max_dim=256):
    """
    Load a dataset image as a batched content tensor for style transfer.
    """

    ds_train_raw, ds_test_raw, ds_info, num_train, num_test = load_tfds_splits()

    example = fetch_raw_example_by_index(
        index=int(index),
        ds_train_raw=ds_train_raw,
        ds_test_raw=ds_test_raw,
        num_train=num_train
    )

    image = tf.cast(example["image"], tf.float32) / 255.0

    return resize_image_preserve_aspect(image, max_dim=max_dim)


